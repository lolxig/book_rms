package com.infoSearch;

public class InfoSearchOfBook {
	
	private String bookId;			// 书号
	private String bookName;		// 书名
	private String bookAuthor;		// 作者
	private String bookPress;		// 出版社
	private String bookStcok;		// 库存量
	
	public String getBookId() {
		return bookId;
	}
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookAuthor() {
		return bookAuthor;
	}
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	public String getBookPress() {
		return bookPress;
	}
	public void setBookPress(String bookPress) {
		this.bookPress = bookPress;
	}
	public String getBookStcok() {
		return bookStcok;
	}
	public void setBookStcok(String bookStcok) {
		this.bookStcok = bookStcok;
	}
	
}
